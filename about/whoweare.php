<p>There's a financial institution in Northeast Georgia where you're more than just a customer. You're an equal member AND you're treated like one!</p>

<p>Founded in 1971 as HALLCO Community Credit Union, we grew into a full-service financial institution offering a wide range of banking products and services with branch locations in Gainesville, Oakwood, Flowery Branch and Cleveland.</p>

<p>As the communities and the needs of the residents in the area around us expanded, so did we. Merging with Habersham Federal Credit Union in 2020 and First Reliance Credit Union in 2021, we have grown in quality and capacity, expanding our branch locations to Homer, Clayton, Clarkesville and Athens.  </p>

<p>Along with this growth, comes new opportunity, new services and our new name – <strong>MyGeorgia Credit Union</strong>. Our name derives from recognition of your credit union ownership, your growth, and your greater community.</p>

<p>Unlike a traditional bank, MyGeorgia is a not-for-profit organization owned by its members and directed by a voluntary board of directors that establishes and reviews policy. The Directors are credit union members who are elected by the membership and serve without pay. MyGeorgia Credit Union offers the utmost in security to our members. Accounts are insured up to $250,000 through the National Credit Union Administration (NCUA), a U. S. government agency.</p>

<p>Our community-chartered credit union exists solely to serve our members' best interests. “Community-chartered” means membership is available to individuals who live and work in Banks, Clarke, Dawson, Habersham, Hall, Jackson, Lumpkin, Madison, Oconee, Oglethorpe, Rabun, and White Counties. We also extend our benefits to your immediate family members. An immediate family member is defined as a spouse, child, sibling, parent, grandparent, and grandchild. To include the changing needs of our members, MyGeorgia’s definition of immediate family has been broadened to include stepparents, stepchildren, stepsiblings, and adoptive relationships. To become a member, an initial deposit of just $5.00 is required.</p>

<p>For over 50 years after the original HALLCO Credit Union was established, MyGeorgia has now evolved to serve the banking needs of more than 20,000 members within a twelve-county district of North Georgia. Setting ourselves apart from other financial institutions, our focus on members instead of profits allows us to offer a full range of financial services specifically designed to meet our members' needs and provide substantial savings with an overall scope of <em>Helping People Afford Life™</em>.</p>


