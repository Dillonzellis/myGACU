              <div class="span12">

				  <p><strong><span style="color: #003DA6">Q.</span>  Are there differences between Habersham Federal Credit Union and MyGeorgia Credit Union products?</strong><br>

					  <strong style="color: #003DA6">A.</strong> Habersham Federal Credit Union and MyGeorgia Credit Union products are very similar, and both entities have worked hard to ensure your banking needs continue to be met with minimal disruption.  See page 4 of this welcome packet for more details regarding products and services.</p>

				  

				  <p><strong><span style="color: #003DA6">Q.</span> Will my member number change?</strong><br>

					  <strong style="color: #003DA6">A.</strong> Yes. Your new MyGeorgia member number will consist of 8 digits and will incorporate your existing HFCU member number within those 8 digits. Your new MyGeorgia member number will start with 2, and end with your existing HFCU member number. Simply fill in the correct number of 0’s in between to get your new MyGeorgia member number.</p>

				  <p>Ex.  If your existing HFCU member number is 123, your new MyGeorgia member number will be 20000123.  If your existing HFCU member number is 123456, your new MyGeorgia member number will be 20123456.</p>

				  

				  <p><strong><span style="color: #003DA6">Q.</span> Can I continue to use my Habersham Federal Credit Union checks?</strong><br>

					  <strong style="color: #003DA6">A.</strong> YES.  You can continue to use your existing supply of HFCU checks after the transition on November 1, 2020.  When it’s time to reorder checks, please contact us to assist you in ordering your new MyGeorgia checks.</p>

				  

				  <p><strong><span style="color: #003DA6">Q.</span> What happens with my direct deposit?</strong><br>

					  <strong style="color: #003DA6">A.</strong> No immediate action is required.  Your existing direct deposits will continue to post as normal. After November 1st,  the Routing/Transit Number for new direct deposits will update to 261174775.</p>

				  

				  <p><strong><span style="color: #003DA6">Q.</span> What happens to my automatic debits that are processed electronically using my account number and routing number?</strong><br>

					  <strong style="color: #003DA6">A.</strong> No immediate action is required. Your automatic debits will continue to post as normal. After November 1st, the Routing/Transit Number for new automatic 

					  debits will update to 261174775.</p>

				  

				  <p><strong><span style="color: #003DA6">Q.</span> Can I continue to use my Habersham Federal Credit Union debit card?</strong><br>

					  <strong style="color: #003DA6">A.</strong> YES. You can continue to use your existing HFCU debit card after November 1, 2020 as you normally would.</p>

				  

				  <p><strong><span style="color: #003DA6">Q.</span> Will I still visit Habersham Federal Credit Union’s website for my online banking needs?</strong><br>

					  <strong style="color: #003DA6">A.</strong> No. As of 5pm Friday October 30, 2020 you will no longer have access to HFCU Online Banking.<br>

Beginning Monday November 2, 2020 you will access your account via the MyGeorgia Mobile app, or if using a PC you can access it via Online Banking at www.HALLCOcu.org.</p>

				  

				  <p><strong><span style="color: #003DA6">Q.</span> When can I download the MyGeorgia Mobile Banking app?</strong><br>

					  <strong style="color: #003DA6">A.</strong> You may download the MyGeorgia Mobile Banking app at any time from the Apple Store or Google Play, but you will not be able to access your accounts and transactions on the app until November 2, 2020.</p>

				  

				  <p><strong><span style="color: #003DA6">Q.</span> How will my Online Bill Pay be affected?</strong><br>

					  <strong style="color: #003DA6">A.</strong> No action is required on your part. Any previously scheduled payments will be processed as usual.</p>

				  

				  <p><strong><span style="color: #003DA6">Q.</span> Will I have access to my e-statements after November 1, 2020?</strong><br>

					  <strong style="color: #003DA6">A.</strong> Yes.  If you are currently enrolled in E-Statements through HFCU you will continue to have access to 18 months of past E-Statements via MyGeorgia’s Online and Mobile banking.</p>

				  

				  <p><strong><span style="color: #003DA6">Q.</span> When will I receive my final Habersham Federal Credit Union statement?</strong><br>

					  <strong style="color: #003DA6">A.</strong> Habersham Federal Credit Union will mail your final statement reflecting your activity and balance through October 31, 2020. Going forward you will receive your regular statements from MyGeorgia Credit Union.</p>

				  

				  <p><strong><span style="color: #003DA6">Q.</span> Will my banking hours change?</strong><br>

					  <strong style="color: #003DA6">A.</strong> All branches will maintain current hours of operation. You will however now have access to a total of 7 branches with some having extended hours of operation. Please visit www.HALLCOcu.org for hours of operation at each of the branches.</p>

	  

