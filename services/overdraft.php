<p>This  notice explains our <U>standard overdraft practices</U>.</p>
<p><STRONG>What are <U>the <EM>standard overdraft practice</EM>s</U> that come with my account?</STRONG></p>
<p>We do authorize and pay  overdrafts for the following types of transactions:</p>
<UL>
  <LI>Checks  and other transactions made using your checking account number</LI>
  <LI>Automatic  bill payments </LI>
</UL>
<p>We <U>will not</U> authorize and pay overdrafts for the following types of transactions unless you  ask us to (see below):</p>
<UL>
  <LI>ATM  transactions</LI>
  <LI>Everyday  debit card transactions</LI>
</UL>
<p>We  pay overdrafts at our discretion, which means we <U>do not guarantee</U> that  we will always authorize and pay any type of transaction. If we <U>do not</U> authorize and pay an overdraft, your transaction will be declined.</p>
<p><STRONG> What fees will I be charged if MyGeorgia Credit Union pays my overdraft? </STRONG></p>
<p>Under our <U>standard overdraft practices</U>:</p>
<UL>
  <LI>We  will charge you a fee of up to $32 each time we pay an overdraft. </LI>
  <LI>There  is <U>no limit</U> on the total fees we can charge you for overdrawing your  account.</LI>
</UL>
