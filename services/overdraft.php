              <p><STRONG>What You Need to Know about Overdrafts  and Overdraft Fees</STRONG></p>
              <p>An <U>overdraft </U>occurs when you do  not have enough money in your account to cover a transaction, but we pay it  anyway. We can cover your overdrafts in two different ways:</p>
              <OL>
  <LI>We  have <U>standard overdraft practices</U> that come with your account.</LI>
  <LI>We  also offer <U>overdraft protection plans</U>, such as a link to a savings  account, which may be less expensive than our standard overdraft practices. To  learn more, ask us about these plans. </LI>
